void LED_Blink(uint32_t delay)
{
	HAL_GPIO_WritePin(PB2_GPIO_Port,PB2_Pin,GPIO_PIN_SET);
	HAL_Delay(delay - 1);
	HAL_GPIO_WritePin(PB2_GPIO_Port,PB2_Pin,GPIO_PIN_RESET);
	if(HAL_GPIO_ReadPin(KEY_GPIO_Port,KEY_Pin) != GPIO_PIN_SET)
		HAL_Delay(500-1);
	else
		HAL_Delay(100-1);
}
